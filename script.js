const lightbox = document.getElementById("imageLightbox");
const lightboxImg = document.getElementById("lightboxImg");
const lightboxCaption = document.getElementById("lightboxCaption");
const lightboxClose = document.getElementById("lightboxClose");

const lightboxVideo = document.getElementById("lightboxVideo");
const lightboxEmbed = document.getElementById("lightboxEmbed");

function buildEmbedHTML(url, provider){
  const encoded = encodeURIComponent(url);
  if(provider === "tiktok"){
    return `<iframe src="https://www.tiktok.com/embed/v2/${url.split("/video/")[1]?.split(/[/?]/)[0] || ''}"
      allow="encrypted-media" allowfullscreen style="width:325px;height:580px;border:0;"></iframe>`;
  }
  return `<iframe src="https://www.facebook.com/plugins/video.php?href=${encoded}&show_text=false"
    style="width:480px;height:auto;aspect-ratio:9/16;border:0;overflow:hidden;" scrolling="no" frameborder="0"
    allowfullscreen allow="autoplay; encrypted-media; picture-in-picture"></iframe>`;
}

function openLightbox({src, type, caption, embedUrl, provider}){
  lightboxImg.style.display = "none";
  lightboxVideo.style.display = "none";
  lightboxVideo.pause();
  lightboxVideo.src = "";
  lightboxEmbed.style.display = "none";
  lightboxEmbed.innerHTML = "";

  if(type === "video"){
    lightboxVideo.src = src;
    lightboxVideo.style.display = "block";
  } else if(type === "embed"){
    lightboxEmbed.innerHTML = buildEmbedHTML(embedUrl, provider);
    lightboxEmbed.style.display = "flex";
  } else {
    lightboxImg.src = src;
    lightboxImg.alt = caption || "";
    lightboxImg.style.display = "block";
  }

  lightboxCaption.textContent = caption || "";
  lightbox.classList.add("active");
  document.body.style.overflow = "hidden";
}

// ---------- CERTIFICATE LINKS → open in lightbox ----------
document.querySelectorAll(".cert-link").forEach(link => {
  link.addEventListener("click", e => {
    e.preventDefault();
    const caption = link.closest(".cert-card")?.querySelector("h4")?.textContent || "";
    openLightbox({ src: link.getAttribute("href"), type: "image", caption });
  });
});

function closeLightbox(){
  lightbox.classList.remove("active");
  lightboxImg.src = "";
  lightboxVideo.pause();
  lightboxVideo.src = "";
  lightboxEmbed.innerHTML = "";
  document.body.style.overflow = "";
}

modalContent.addEventListener("click", e => {
  const img = e.target.closest(".evidence-img:not(.evidence-video):not(.evidence-embed) img");
  const vid = e.target.closest(".evidence-video video");
  const embedCard = e.target.closest(".evidence-embed");

  if(img){
    e.stopPropagation();
    const caption = img.closest(".evidence-img").querySelector("figcaption")?.textContent || img.alt;
    openLightbox({src: img.src, type: "image", caption});
  } else if(vid){
    e.stopPropagation();
    const caption = vid.closest(".evidence-video").querySelector("figcaption")?.textContent || "";
    openLightbox({src: vid.src, type: "video", caption});
  } else if(embedCard){
    e.stopPropagation();
    const caption = embedCard.querySelector("figcaption")?.textContent || "";
    const embedUrl = decodeURIComponent(embedCard.dataset.embedUrl || "");
    const provider = embedCard.dataset.provider || "";
    openLightbox({type: "embed", caption, embedUrl, provider});
  }
});

